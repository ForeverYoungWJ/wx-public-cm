<template>
  <div class="HzDetail">
    <van-nav-bar
      :title="titleText"
      left-text=""
      left-arrow
      @click-left="onClickLeft"
    />
    <van-swipe :autoplay="5000"
               :duration="1200"
               loop
               class="swiper-box">
      <van-swipe-item v-for="(image, index) in images" :key="index">
        <img :src="image" />
