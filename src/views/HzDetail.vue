    </van-swipe>
    <div class="abs info-box">
      <div class="info-text" v-html="infoText">{{infoText}}</div>
    </div>
  </div>
</template>

<script>
  import { NavBar } from 'vant';
  import { Swipe, SwipeItem } from 'vant';
  import {Toast} from 'vant';

  let Id;
  export default {
    name: 'HzDetail',

    //组件
    components: {
      [NavBar.name]: NavBar,
      [Swipe.name]: Swipe,
      [SwipeItem.name]: SwipeItem,
      [Toast.name]: Toast,
    },

    //初始数据
    data() {
      return {
        titleText:'',
        images: [],
        infoText:''
      }
    },

    //创建前
    beforeCreate() {
    },

    //创建后
    created() {
      Id = this.$route.query.id;
      this._getInfo();
    },

    //挂在开始之前被调用
    beforeMount() {
    },

    //挂在完成 只会执行一次。
    mounted() {
    },

    //更新前
    beforeUpdata() {
    },

    //更新后
    updated() {
    },

    //销毁前
    beforeDestrioy() {
    },

    //销毁后
    destroyed() {
    },

    //监听
    watch: {},

    //调用的方法
    methods: {
      onClickLeft() {
        this.$router.push('ny')//home
      },

      //根据id查询
      _getInfo: function () {
        let self = this;
        self.$axios.request({
          url: 'http://cm.legions.tech/api/cm/news/get',
          data: {
            id:Id
          }
        }).then((res) => {
          let code = parseInt(res.code); //parseInt
          if (code === 1) {
            let list = res.data.images.split(",");
            let imgList = [];
            list.forEach((item, index, arr) => {
              let Item;
              Item = process.env.VUE_APP_wxImgUrl + item;
              imgList.push(Item);
            });
            self.titleText= res.data.title;
            self.images = imgList;
            self.infoText= res.data.content;
          } else {
            Toast.fail(res.msg);
          }
        }).catch(err => {
          Toast.fail(err.msg || '请求失败');
        });
      },
    },

  }
</script>

